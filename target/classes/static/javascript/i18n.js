function setLanguage(lang) {
    localStorage.setItem('preferred-lang', lang);
    applyTranslations(lang);
}

// Function to translate the whole page or a specific part of it
function translatePage(target = document) {
    const lang = localStorage.getItem('preferred-lang') || 'ar';
    const t = translations[lang];
    if (!t) return;

    // Apply translations to elements with data-i18n attribute
    target.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else if (el.tagName === 'OPTION') {
                el.text = t[key];
            } else {
                // Preserve icons if present
                const icon = el.querySelector('i');
                if (icon) {
                    // Create a text node with the translation
                    const textNode = document.createTextNode(' ' + t[key]);
                    el.innerHTML = '';
                    el.appendChild(icon);
                    el.appendChild(textNode);
                } else {
                    el.innerHTML = t[key];
                }
            }
        }
    });

    // Special handling for placeholders
    target.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });

    // Titles
    target.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (t[key]) {
            el.title = t[key];
        }
    });
}

function getTranslation(key) {
    const lang = localStorage.getItem('preferred-lang') || 'ar';
    return translations[lang][key] || key;
}

function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    console.log('Applying language:', lang, 'Direction:', t.dir);

    // Update direction and HTML lang attribute
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', t.dir);
    
    // Update body classes for layout fixes
    if (t.dir === 'ltr') {
        document.body.classList.add('ltr');
        document.body.classList.remove('rtl');
    } else {
        document.body.classList.add('rtl');
        document.body.classList.remove('ltr');
    }
    
    // Update Bootstrap CSS based on direction
    const bootstrapLink = document.querySelector('link[href*="bootstrap"]');
    if (bootstrapLink) {
        const oldHref = bootstrapLink.href;
        const newHref = t.dir === 'ltr' 
            ? "https://cdn.jsdelivr.net/npm/bootstrap@5.3.0/dist/css/bootstrap.min.css"
            : "https://cdn.jsdelivr.net/npm/bootstrap@5.3.0/dist/css/bootstrap.rtl.min.css";
        
        if (oldHref !== newHref) {
            bootstrapLink.href = newHref;
            console.log('Swapped Bootstrap CSS to:', newHref);
        }
    }

    // Call translatePage for the whole document
    translatePage(document);

    // Special handling for platform name in navbar (icon + text)
    document.querySelectorAll('.navbar-brand').forEach(brand => {
        const icon = brand.querySelector('i');
        if (icon) {
            // Keep the icon and just update the text content
            // We empty the brand and re-add icon + text to ensure correct order based on direction
            const text = document.createTextNode(' ' + t['platform-name']);
            brand.innerHTML = '';
            brand.appendChild(icon);
            brand.appendChild(text);
        } else {
            brand.textContent = t['platform-name'];
        }
    });

    // Update language dropdown text
    const langLabel = document.getElementById('langLabel');
    if (langLabel) {
        langLabel.textContent = t['language'];
    }

    // Update checkmarks in dropdown
    document.querySelectorAll('.lang-item i.fa-check').forEach(check => {
        check.style.visibility = 'hidden';
    });
    const activeCheck = document.querySelector(`.lang-item[onclick*="setLanguage('${lang}')"] i.fa-check`);
    if (activeCheck) {
        activeCheck.style.visibility = 'visible';
    }
}

